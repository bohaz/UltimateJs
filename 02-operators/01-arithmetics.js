let a = 5;
let b = 7;

console.log (a + b); // sum
console.log (a - b); // difference
console.log (a * b); // product
console.log (a / b); // quotient
console.log (a % b); // remainder
console.log (a ** b); // exponentiation

// Increment and decrement operators

let c = 5;
console.log(++c);
console.log(c++);

console.log(--c);
console.log(c--);